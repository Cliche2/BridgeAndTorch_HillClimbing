import math
from time import sleep


def f(s):
    s.sort()
    if len(s) > 3:
        a = s[0] + s[-1] + min(2 * s[1], s[0] + s[-2])
        return a + f(s[:-2])
    else:
        return sum(s[len(s) == 2:])


def evaluate(right, left, toMove):
    cost = max(toMove)
    right.extend(toMove)
    left = [_ for _ in left if _ not in toMove]
    returnCost = min(right)
    right.remove(returnCost)
    left.append(returnCost)
    rSum = sum(left)
    totalValue = cost + returnCost + rSum
    return totalValue, right, left, (cost + returnCost), [returnCost]


def hillClimbing(persons, movement, paint, right=[], left=[], total=0):
    if len(left) == 0:
        left = persons[:]
    left.sort()
    toMove = []
    minValue = math.inf
    currentCost = 0
    toReturn = []

    if len(left) == 2:
        toMove = left
        cost = max(left)
        right.extend(left)
        nLeft = []
        nRight = right
        currentCost = cost
        minValue = currentCost
    else:
        for a in left:
            _ = left[:]
            _.remove(a)
            for b in _:

                paint([a, b], "blue")

                totalValue, cRight, cLeft, totalCost, cReturn = evaluate(right[:], left[:], [a, b])
                if (totalValue < minValue):
                    minValue = totalValue
                    toMove = [a, b]
                    nRight = cRight
                    nLeft = cLeft
                    currentCost = totalCost
                    toReturn = cReturn

                paint([a, b], "white")

    print("______MOVE______")
    print(minValue)
    print(toMove)
    print(nRight)
    print(nLeft)
    print("Return: ", toReturn)

    paint(toMove, "green")

    total = total + currentCost

    # Move to the Right
    movement(toMove, "right")

    # Move to the Left
    if len(toReturn) > 0:
        movement(toReturn, "left")


    if sum(nRight) == sum(persons):
        print("______COST______")
        print("COST: ", total)
    else:
        hillClimbing(persons, movement, paint, nRight, nLeft, total)

